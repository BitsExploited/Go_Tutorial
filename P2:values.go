package main
import "fmt"

func main(){

  fmt.Println("Go" + "Lang")  // Adding two strings
  fmt.Println("43 + 34 = ", 43+34) // Adding two numbers
  fmt.Println("53/34", 53/34) // Dividing two numbers
  fmt.Println(true && false) // AND Operator
  fmt.Println(true || false) // OR Operator
  fmt.Println(!true) // ! is NOT operator
 
}
